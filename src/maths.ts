const addNumbers = (a: number, b: number): number => a + b;

const subtractNumbers = (a: number, b: number): number => a - b;

const multiplyNumbers = (a: number, b: number): number => a * b;

const divideNumbers = (a: number, b: number): number => a / b;

const findSquareRoot = (a: number): number => Math.sqrt(a);

export { addNumbers, subtractNumbers, multiplyNumbers, divideNumbers, findSquareRoot };